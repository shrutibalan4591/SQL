/***************** QUERY ******************/
select event_type, customer, campaign, total
from (select status as event_type, 
			   concat(first_name, ' ', last_name) as customer, 
			   group_concat(distinct name) as campaign, 
			   count(*) as total,
			   rank() over (partition by status order by count(1) desc) as rank_
		from customers c join campaigns ca on c.id=ca.customer_id
						 join events e on ca.id=e.campaign_id
		group by event_type, customer) as rank_table
where rank_=1
;